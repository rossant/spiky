"""Unit tests for logger module."""

# -----------------------------------------------------------------------------
# Imports
# -----------------------------------------------------------------------------

from spiky.control.logger import Logger


# -----------------------------------------------------------------------------
# Tests
# -----------------------------------------------------------------------------
def test_logging_string():
    l = Logger(fmt='')
    l.write("test 1")
    l.write("test 2")
    
    log = l.get_log()
    assert log == "test 1\ntest 2\n"
    